n = int(input())
if n == 1:
    day = "Monday"
elif n == 2 :
    day = "Tuesday"
elif n == 3 :
    day = "Wednesday"
elif n == 4 :
    day = "Thursday"
elif n == 5 :
    day = "Friday"
elif n == 6 :
    day = "Saturday"
elif n == 7 :
    day = "Sunday"
else:
    day = "Error"
print(day)